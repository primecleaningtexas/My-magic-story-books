export function generateStory(name, theme) {
  const pages = [
    `Once upon a time, ${name} discovered a magical world full of wonder.`,
    `${name} met new friends and learned the value of kindness.`,
    `Together, they faced small challenges with courage.`,
    `${name} never gave up, even when things were hard.`,
    `A magical surprise appeared just when hope was needed.`,
    `With a brave heart, ${name} helped everyone.`,
    `The world became brighter because of ${name}.`,
    `This is only the beginning of ${name}'s magical story.`
  ];

  return pages;
}
